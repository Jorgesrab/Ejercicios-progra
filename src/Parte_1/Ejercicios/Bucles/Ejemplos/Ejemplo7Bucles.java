package Parte_1.Ejercicios.Bucles.Ejemplos;

/*
BUCLES FOR ANIDADOS
 */
public class Ejemplo7Bucles {
    public static void main(String[] args) {
        int num1=3;
        int num2=2;
        for(int i = 1; i <= num1; i++) {
            for(int j = 1; j <= num2; j++) {
                System.out.print(j);
            }
        }
    }
}
