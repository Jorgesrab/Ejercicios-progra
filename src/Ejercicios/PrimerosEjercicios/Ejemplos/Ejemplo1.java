package Ejercicios.PrimerosEjercicios.Ejemplos;

/*
    Explicación de estructura de un programa

    donde está nuestra clase, lo nombal es que en este package haya más de una clase
    nos permitirá organizar nuestro códiogo de forma que las clases relacionadas
    estén en un mismo paquete.
*/
public class Ejemplo1 {//nombre la clase, coincide con el mismo fichero
    //mínimo una clase public en un programa

    public static void main(String[] args) {  //método principal y que buscará Java a la hora de ejecutar
        //si no existe un método main, no se ejecutará
        //su encabezado es siempre igual, el propio IDE la escribe automáticamente
        System.out.println("Hola Mundo, soy Marina Pinteño");  //es una instrucción
    }
}

